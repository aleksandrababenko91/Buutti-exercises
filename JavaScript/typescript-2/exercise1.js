function calc(num1, num2) {
    return (num1 + num2);
}
console.log(calc(2, 3));
//anonymous function
var calcAnonymous = function (num1, num2) {
    return (num1 + num2);
};
console.log(calcAnonymous(4, 8));
//arrow function 
var calcArrow = function (num1, num2) {
    return (num1 + num2);
};
console.log(calcArrow(188, 155));
